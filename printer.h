      void DrawImage(const int& x,
                     const int& y,
                     SDL_Texture* tex)
      {
        int tw, th;
        SDL_QueryTexture(tex, nullptr, nullptr, &tw, &th);

        SDL_Rect src;
        src.x = 0;
        src.y = 0;
        src.w = tw;
        src.h = th;

        SDL_Rect dst;
        dst.x = x;
        dst.y = y;
        dst.w = tw;
        dst.h = th;

        if (SDL_GetRenderTarget(_rendererRef) == nullptr)
        {
          SDL_SetRenderTarget(_rendererRef, _frameBuffer);
        }

        SDL_RenderCopy(_rendererRef, tex, &src, &dst);
      }

